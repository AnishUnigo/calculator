package com.anish.util;


public class Calculator  {		
	
	
	public int add(int x, int y){
		System.out.println("x+y = "+(x+y));
		return x+y;
	}
	
	public int substract(int x, int y){
		System.out.println("x-y = "+(x-y));
		return x-y;
	}
	
	public int multiply(int x, int y){
		System.out.println("x*y = "+(x*y));
		return x*y;
	}
	
	public int div(int x, int y){
		return x/y;
	}

}
